    public void change() {
        double sum = 0;
        double tax = 5;
        double cash = Double.parseDouble(Ccash.getText());
        for (int i = 0; i < billData.getRowCount(); i++) {
            sum = sum + Double.parseDouble(billData.getValueAt(i, 4).toString());
        }
        double cTax = (sum * 5) / 100;
        double cChange = (cash - (sum + cTax));
        String changeGiven = String.format("%.2f BDT", cChange);
        Cchange.setText(changeGiven);
    }
