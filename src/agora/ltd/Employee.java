    public void ItemCost() {
        double sum = 0;
        for (int i = 0; i < billData.getRowCount(); i++) {
            sum = sum + Double.parseDouble(billData.getValueAt(i, 4).toString());
        }
        netTotal.setText(Double.toString(sum));
        double Ctotal = Double.parseDouble(netTotal.getText());
        double Ctax = (Ctotal * 5) / 100;
        String iTaxTotal = String.format("%.2f BDT", Ctax);
        tax.setText(iTaxTotal);
        String iNetTotal = String.format("%.2f BDT", Ctotal);
        netTotal.setText(iNetTotal);
        String iTotal = String.format("%.2f BDT", Ctotal + Ctax);
        total.setText(iTotal);
        amount.setText(iTotal);
    }

